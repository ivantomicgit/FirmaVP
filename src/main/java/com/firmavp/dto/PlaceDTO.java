package com.firmavp.dto;

public class PlaceDTO {

	private int id;
	private String placeName;
	private String placeAddress;
	private String placeContact;
	private String placeColor;
		
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPlaceName() {
		return placeName;
	}
	public void setPlaceName(String placeName) {
		this.placeName = placeName;
	}
	public String getPlaceAddress() {
		return placeAddress;
	}
	public void setPlaceAddress(String placeAddress) {
		this.placeAddress = placeAddress;
	}
	public String getPlaceContact() {
		return placeContact;
	}
	public void setPlaceContact(String placeContact) {
		this.placeContact = placeContact;
	}
	public String getPlaceColor() {
		return placeColor;
	}
	public void setPlaceColor(String placeColor) {
		this.placeColor = placeColor;
	}
	
}
